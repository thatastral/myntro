/**
 * Admin client using raw Supabase JS — bypasses RLS entirely via service role key.
 * Use for server-only operations where no user context is needed.
 */
export function createAdminClient() {
  return createSupabaseClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!,
    {
      global: {
        // Opt out of Next.js fetch cache — prevents one user's data
        // being served to a different user from a cached response.
        fetch: (url, options = {}) =>
          fetch(url, { ...options, cache: 'no-store' }),
      },
    },
  )
}
